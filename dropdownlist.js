            resultsDiv.style.overflowY = 'auto';
            resultsDiv.style.background = '#fff';
            resultsDiv.style.display = 'none'; // Initially hide the results

            // Append search input and results div to the container
            var container = document.querySelector('.algo_vaccines');
            container.appendChild(searchInput);
            container.appendChild(resultsDiv);

            // Info div for displaying selected country's vaccination info
            var infoDiv = document.createElement('div');
            container.appendChild(infoDiv);

            searchInput.addEventListener('input', function () {
                var searchValue = this.value.toUpperCase();
                resultsDiv.innerHTML = ''; // Clear previous results
                if (searchValue) {
                    resultsDiv.style.display = 'block';
                    for (var country in data) {
                        if (country.toUpperCase().includes(searchValue)) {
                            var resultOption = document.createElement('div');
                            resultOption.textContent = country;
                            resultOption.style.padding = '10px';
                            resultOption.style.cursor = 'pointer';
                            resultOption.style.borderBottom = '1px solid #eee';
                            resultOption.style.backgroundColor = 'white';
                            resultOption.addEventListener('click', function () {
                                searchInput.value = this.textContent; // Set input value to the selected country
                                resultsDiv.style.display = 'none'; // Hide the results
                                displayVaccinationInfo(this.textContent); // Display vaccination info for the selected country
                            });
                            resultsDiv.appendChild(resultOption);
                        }
                    }
                } else {
                    resultsDiv.style.display = 'none'; // Hide results if input is empty
                }
            });

            function displayVaccinationInfo(country) {
                var vaccines = data[country];
                var content = '<span style="font-size: 16px;">Vaccinations for <b>' + country + '</b>:</span>';
                if (vaccines) {
                    vaccines.forEach(function (vaccine) {
                        var pillColor = vaccine.Type.includes('Most Travellers') ? 'red' : vaccine.Type.includes('Some Travellers') ? 'orange' : 'gray';
                        content += '<p><b>' + vaccine.Vaccine + '</b> <span style="font-size: 13px; background-color: ' + pillColor + '; color: white; padding: 3px 8px; border-radius: 15px; display: inline-block;">' + vaccine.Type + '</span></p>';
                    });
                } else {
                    content = '<p>No vaccination information available for this country.</p>';
                }
                infoDiv.innerHTML = content;
            }
